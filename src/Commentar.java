public class Commentar {
    public static void main(String[] args) {
        System.out.println(sum(5, 5));
    }

    /**
     * 
     * @param value1
     * @param value2
     * @return
     */

    static int sum(int value1, int value2) {
        // oprator pertambahan
        return value1 + value2;
    }
}
